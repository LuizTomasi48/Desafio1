     public void carregaTabela() {
        
            DefaultTableModel modelo = (DefaultTableModel) jtUsuario.getModel();
            modelo.setNumRows(0);
            
            jtUsuario.getColumnModel().getColumn(0).setPreferredWidth(15);
            
            List<Usuario> usuarios = new ArrayList<Usuario>();
            
            UsuarioC usuarioC = new UsuarioC();
            usuarios = usuarioC.validaUsuario(txtEmailU.getText(), valueOf(txtSenha.getPassword()));
            
            for(Usuario u : usuarios){
                modelo.addRow(new Object[]{u.getId(), u.getNome(), u.getEmail(),u.getEndereco(), u.getSenha() , u.getCpf() , u.getAtivo(), u.getPergunta1()});
            }    
        }
    
